import {
  copyFile,
  mkdir,
  readFile,
  readdir,
  unlink,
  writeFile,
} from 'node:fs/promises';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

import { z } from 'zod';

const localeFilenamePattern = /^([a-z]{2,3})\.json$/;
const localeCatalogSchema = z.object({
  _meta: z.object({
    languageName: z.string().trim().min(1),
  }),
}).loose();

const repositoryRoot = path.resolve(import.meta.dirname, '../../..');
const defaultPaths = {
  sourceDirectory: path.join(repositoryRoot, 'apps/web/src/i18n/locales'),
  destinationDirectory: path.join(repositoryRoot, 'apps/mobile/src/i18n/locales'),
  registryPath: path.join(repositoryRoot, 'apps/mobile/src/i18n/generated.ts'),
};

export class TranslationSyncError extends Error {
  constructor(message, options) {
    super(message, options);
    this.name = 'TranslationSyncError';
  }
}

function compareLocales(left, right) {
  if (left.code === 'en') return -1;
  if (right.code === 'en') return 1;
  if (left.code < right.code) return -1;
  if (left.code > right.code) return 1;
  return 0;
}

function quoteTypeScript(value) {
  return `'${value
    .replaceAll('\\', '\\\\')
    .replaceAll("'", "\\'")
    .replaceAll('\r', '\\r')
    .replaceAll('\n', '\\n')}'`;
}

export async function discoverWebTranslations(sourceDirectory) {
  const entries = await readdir(sourceDirectory, { withFileTypes: true });
  const localeEntries = entries.filter((entry) => entry.isFile() && entry.name.endsWith('.json'));
  const locales = await Promise.all(localeEntries.map(async (entry) => {
    const filenameMatch = localeFilenamePattern.exec(entry.name);
    if (!filenameMatch) {
      throw new TranslationSyncError(
        `${entry.name} must use a lowercase 2-3 letter locale code.`,
      );
    }

    const code = filenameMatch[1];
    const sourcePath = path.join(sourceDirectory, entry.name);
    const source = await readFile(sourcePath, 'utf8');
    let catalog;
    try {
      catalog = JSON.parse(source);
    } catch (error) {
      if (error instanceof SyntaxError) {
        throw new TranslationSyncError(`${entry.name} contains malformed JSON.`, { cause: error });
      }
      throw error;
    }

    const parsedCatalog = localeCatalogSchema.safeParse(catalog);
    if (!parsedCatalog.success) {
      throw new TranslationSyncError(
        `${entry.name} must contain a non-empty _meta.languageName.`,
      );
    }

    return {
      code,
      filename: entry.name,
      languageName: parsedCatalog.data._meta.languageName,
      sourcePath,
    };
  }));

  if (!locales.some((locale) => locale.code === 'en')) {
    throw new TranslationSyncError('en.json is required.');
  }

  return locales.toSorted(compareLocales);
}

export function generateTranslationRegistry(locales) {
  const imports = locales
    .map(({ filename }, index) => (
      `import locale${index} from ${quoteTypeScript(`./locales/${filename}`)};`
    ))
    .join('\n');
  const catalogs = locales
    .map(({ code }, index) => `  ${quoteTypeScript(code)}: locale${index},`)
    .join('\n');
  const languageOptions = locales
    .map(({ code, languageName }) => (
      `  { code: ${quoteTypeScript(code)}, label: ${quoteTypeScript(languageName)} },`
    ))
    .join('\n');
  const languageCodes = locales.map(({ code }) => `  ${quoteTypeScript(code)},`).join('\n');

  return `// Generated by npm run i18n:sync. Do not edit.\n${imports}\n\n`
    + `export const localeCatalogs = {\n${catalogs}\n} as const;\n\n`
    + `export const LANGUAGE_OPTIONS = [\n${languageOptions}\n] as const;\n\n`
    + `export const SUPPORTED_LANGUAGES = [\n${languageCodes}\n] as const;\n`;
}

async function readTextIfExists(filePath) {
  try {
    return await readFile(filePath, 'utf8');
  } catch (error) {
    if (error instanceof Error && 'code' in error && error.code === 'ENOENT') return undefined;
    throw error;
  }
}

async function readDirectoryIfExists(directory) {
  try {
    return await readdir(directory, { withFileTypes: true });
  } catch (error) {
    if (error instanceof Error && 'code' in error && error.code === 'ENOENT') return [];
    throw error;
  }
}

export async function syncWebTranslations(paths) {
  const locales = await discoverWebTranslations(paths.sourceDirectory);
  await mkdir(paths.destinationDirectory, { recursive: true });
  await mkdir(path.dirname(paths.registryPath), { recursive: true });

  const managedFilenames = new Set(locales.map(({ filename }) => filename));
  const destinationEntries = await readdir(paths.destinationDirectory, { withFileTypes: true });
  const staleCatalogs = destinationEntries.filter((entry) => (
    entry.isFile() && entry.name.endsWith('.json') && !managedFilenames.has(entry.name)
  ));

  await Promise.all(staleCatalogs.map((entry) => (
    unlink(path.join(paths.destinationDirectory, entry.name))
  )));
  await Promise.all(locales.map(({ filename, sourcePath }) => (
    copyFile(sourcePath, path.join(paths.destinationDirectory, filename))
  )));
  await writeFile(paths.registryPath, generateTranslationRegistry(locales));

  return { languageCodes: locales.map(({ code }) => code) };
}

export async function checkWebTranslations(paths) {
  const locales = await discoverWebTranslations(paths.sourceDirectory);
  const driftedPaths = [];
  const expectedRegistry = generateTranslationRegistry(locales);
  const actualRegistry = await readTextIfExists(paths.registryPath);
  if (actualRegistry !== expectedRegistry) driftedPaths.push(paths.registryPath);

  const managedFilenames = new Set(locales.map(({ filename }) => filename));
  const destinationEntries = await readDirectoryIfExists(paths.destinationDirectory);
  for (const entry of destinationEntries) {
    if (entry.isFile() && entry.name.endsWith('.json') && !managedFilenames.has(entry.name)) {
      driftedPaths.push(path.join(paths.destinationDirectory, entry.name));
    }
  }

  for (const locale of locales) {
    const source = await readFile(locale.sourcePath, 'utf8');
    const destination = await readTextIfExists(
      path.join(paths.destinationDirectory, locale.filename),
    );
    if (destination !== source) {
      driftedPaths.push(path.join(paths.destinationDirectory, locale.filename));
    }
  }

  if (driftedPaths.length > 0) {
    throw new TranslationSyncError(`Translation output is out of date: ${driftedPaths.join(', ')}`);
  }

  return { languageCodes: locales.map(({ code }) => code) };
}

const invokedPath = process.argv[1] ? path.resolve(process.argv[1]) : undefined;
const isCommandLine = invokedPath === fileURLToPath(import.meta.url);

if (isCommandLine) {
  try {
    if (process.argv.includes('--check')) {
      await checkWebTranslations(defaultPaths);
      console.log('Mobile translations are up to date.');
    } else {
      await syncWebTranslations(defaultPaths);
      console.log('Synced BillManager web translations for mobile.');
    }
  } catch (error) {
    console.error(error instanceof Error ? error.message : String(error));
    process.exitCode = 1;
  }
}
